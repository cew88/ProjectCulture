// Home hover
$(document).ready(function()
      {
        $(document).mousemove(function()
        {
          if($("#Home:hover").length != 0)
          {
             document.getElementById("Home").classList.add('active');
          } 
          else
          {
            document.getElementById("Home").classList.remove('active');
          }
        });
      });
